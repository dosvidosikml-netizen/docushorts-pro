function buildUserPrompt({ topic, tone, duration, retryHint = "" }) {
  const wordsTarget = Math.round(duration * 2.2);
  return `
Напиши вирусный сценарий на русском для Shorts/Reels/TikTok${duration > 180 ? " (long-form формат)" : ""}.

Тема: ${topic}
Тон: ${tone}
Длительность: ${duration} секунд${duration > 180 ? ` (${Math.round(duration / 60)} минут — long-form)` : ""}
Целевой объём: ~${wordsTarget} слов

Соблюдай 4-актную структуру: Hook → Build → Climax → Outro+Вопрос.
${duration > 180 ? `Для long-form ролика растяни Build на несколько мини-актов: каждые 60-90 секунд — новый поворот / новый факт / новый угол. Не дай зрителю заскучать.` : ""}
Применяй show-don't-tell: абстракции переводи в конкретные физические образы.
Чередуй длину предложений для пульса — обязательно есть короткие ударные фразы (3-5 слов).
Минимум 2-3 раза обратись к зрителю на "ты".
Climax — отдельная короткая фраза, не растворяй в абзаце.
Если хочется перечислить версии/причины/последствия — выбери ОДНУ самую яркую и оставь её. Списки через запятую = зритель забыл.
Только текст диктора. Никаких заголовков актов в тексте.
${retryHint}
`;
}

export async function POST(req) {
  try {
    const body = await req.json();
    const topic = String(body.topic || "").trim();
    const tone = String(body.tone || "cinematic documentary thriller").trim();
    const duration = Number(body.duration || 60);

    const wordsTarget = Math.round(duration * 2.2);
    const maxTokensForScript = Math.max(2000, Math.ceil(wordsTarget * 2.5 * 1.2));

    if (!process.env.OPENROUTER_API_KEY) {
      const fallback = fallbackScript({ topic, duration });
      return Response.json({
        text: fallback,
        validation: validateScript(fallback),
        warning: "OPENROUTER_API_KEY not set — using local fallback",
      });
    }

    // ── Попытка #1 ──
    let attempt = 1;
    let result = await callOpenRouter({
      taskType: TASK_TYPES.SCRIPT_WRITING,
      systemPrompt: SYSTEM_PROMPT,
      userMessage: buildUserPrompt({ topic, tone, duration }),
      maxTokensOverride: maxTokensForScript,
      appTitle: "NeuroCine Script Writer v2.4",
    });

    if (!result.ok) {
      return Response.json({
        text: fallbackScript({ topic, duration }),
        validation: null,
        warning: result.error,
      });
    }

    let text = result.content || "";
    let validation = validateScript(text);
    let modelUsed = result.model_used;
    const attempts = [{ attempt: 1, score: validation.score, issues: validation.issues }];

    // ── Авто-ретрай если критичные проверки провалены ──
    // Ретраим если: hook слабый ИЛИ нет you-address ИЛИ outro слабый
    // Эти три — самые критичные для вирусности
    const needsRetry = (v) => !v.checks.hook_strong || !v.checks.has_you_address || !v.checks.outro_strong;

    while (attempt < 3 && needsRetry(validation)) {
      attempt += 1;
      const retryHint = buildRetryHint(validation);
      const retryResult = await callOpenRouter({
        taskType: TASK_TYPES.SCRIPT_WRITING,
        systemPrompt: SYSTEM_PROMPT,
        userMessage: buildUserPrompt({ topic, tone, duration, retryHint }),
        maxTokensOverride: maxTokensForScript,
        temperatureOverride: 0.55, // чуть выше temp на ретрае для разнообразия
        appTitle: `NeuroCine Script Writer v2.4 (retry ${attempt})`,
      });

      if (!retryResult.ok) break;

      const retryText = retryResult.content || "";
      const retryValidation = validateScript(retryText);
      attempts.push({ attempt, score: retryValidation.score, issues: retryValidation.issues });

      // Берём результат если он лучше
      if (retryValidation.score > validation.score) {
        text = retryText;
        validation = retryValidation;
        modelUsed = retryResult.model_used;
      }
    }

    return Response.json({
      text: text || fallbackScript({ topic, duration }),
      validation,
      attempts,
      model_used: modelUsed,
    });
  } catch (e) {
    return Response.json({
      text: fallbackScript({ topic: "", duration: 60 }),
      validation: null,
      warning: e.message,
    });
  }
}
